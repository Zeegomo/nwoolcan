package nwoolcan.model.utils;

/**
 * Enum used to identify the various unit of measures.
 */
public enum UnitOfMeasure {
    /**
     * Grams.
     */
    GRAM,
    /**
     * Milliliters.
     */
    MILLILITER,
    /**
     * Basic units.
     */
    UNIT,
    /**
     * Percentage.
     */
    PERCENTAGE,
    /**
     * Celsius degrees.
     */
    CELSIUS_DEGREE,
    /**
     * Milligrams per liter.
     */
    MILLIGRAMS_PER_LITER,
    /**
     * Pure number.
     */
    UNITLESS,
    /**
     * Ebc special unit of measure.
     */
    EBC,
    /**
     * IBU special unit of measure.
     */
    IBU;
}
